uint32_t sems_add_service_uuid(uint16_t uuid,uint8_t *uuid_type,uint16_t *ble_service_handle){
    // Add service.
    uint32_t err_code;
    ble_uuid_t ble_uuid;
  
    ble_uuid128_t base_uuid = {LBS_SEMS_UUID_BASE};
    err_code = sd_ble_uuid_vs_add(&base_uuid, &ble_uuid.type);
    VERIFY_SUCCESS(err_code);
    
    *uuid_type=ble_uuid.type;
    ble_uuid.uuid = uuid;

    return sd_ble_gatts_service_add(BLE_GATTS_SRVC_TYPE_PRIMARY, &ble_uuid, ble_service_handle);
}

uint32_t sems_add_char_uuid(uint32_t uuid,
                            uint8_t *uuid_type,
                            uint16_t *ble_service_handle,
                            ble_gatts_char_handles_t *ble_char_handle,
                            uint8_t can_read,
                            uint8_t can_write,
                            uint32_t maxlen){
    ble_gatts_char_md_t char_md;
    ble_gatts_attr_t    attr_char_value;
    ble_uuid_t          ble_uuid;
    ble_gatts_attr_md_t attr_md;
    
    memset(&char_md, 0, sizeof(char_md));
    
    char_md.char_props.read   = can_read;
    char_md.char_props.write   = can_write;
    char_md.p_char_user_desc  = NULL;
    char_md.p_char_pf         = NULL;
    char_md.p_user_desc_md    = NULL;
    char_md.p_cccd_md         = NULL;
    char_md.p_sccd_md         = NULL;

    ble_uuid.type = *uuid_type;
    ble_uuid.uuid = uuid;

    memset(&attr_md, 0, sizeof(attr_md));
    
    if(can_read){
      BLE_GAP_CONN_SEC_MODE_SET_OPEN(&attr_md.read_perm);
    }else{
      BLE_GAP_CONN_SEC_MODE_SET_NO_ACCESS(&attr_md.read_perm);
    }
    if(can_write){
      BLE_GAP_CONN_SEC_MODE_SET_OPEN(&attr_md.write_perm);
    }else{
      BLE_GAP_CONN_SEC_MODE_SET_NO_ACCESS(&attr_md.write_perm);
    }
    attr_md.vloc       = BLE_GATTS_VLOC_STACK;
    attr_md.rd_auth    = 0;
    attr_md.wr_auth    = 0;
    attr_md.vlen       = 1;

    memset(&attr_char_value, 0, sizeof(attr_char_value));

    attr_char_value.p_uuid       = &ble_uuid;
    attr_char_value.p_attr_md    = &attr_md;
    attr_char_value.init_len     = sizeof(uint32_t);
    attr_char_value.init_offs    = 0;
    attr_char_value.max_len      = maxlen;
    attr_char_value.p_value      = NULL;

    return sd_ble_gatts_characteristic_add(*ble_service_handle,
                                               &char_md,
                                               &attr_char_value,
                                               ble_char_handle);
}

void sems_update_advertising_response_data(void* scan_response_data, uint16_t data_length)
{
    uint32_t err_code;
    ble_advdata_t   advdata_response;
    // Build advertising data struct to pass into @ref ble_advertising_init.
    memset(&advdata_response, 0, sizeof(advdata_response));
    // Prepare the scan response Manufacturer specific data packet
    if(scan_response_data!=NULL && data_length>0){
        // Populate the scan response packet
        advdata_response.name_type               = BLE_ADVDATA_NO_NAME; 
        ble_advdata_service_data_t srv_data;
        advdata_response.p_service_data_array = &srv_data;    
        advdata_response.p_service_data_array->service_uuid = ADVERTISING_SERVICE_UUID;
        advdata_response.p_service_data_array->data.p_data = scan_response_data;
        advdata_response.p_service_data_array->data.size   = data_length;
        advdata_response.service_data_count = 1;
        
    }else
    {
        advdata_response.name_type               = BLE_ADVDATA_NO_NAME;  
        advdata_response.service_data_count = 0;
    }
    err_code = ble_advdata_set(NULL,&advdata_response);
    NRF_LOG_INFO("advdata set result:%d \n",err_code);
    NRF_LOG_FLUSH();
}